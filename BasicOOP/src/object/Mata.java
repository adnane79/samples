package object;


public class Mata {

    private String jenisMata;

    private Boolean isBeleken;

    private String warnaBolaMata;

    /**
     * @return the isBeleken
     */
    public Boolean getIsBeleken() {
        return isBeleken;
    }

    /**
     * @param isBeleken the isBeleken to set
     */
    public void setIsBeleken(Boolean isBeleken) {
        this.isBeleken = isBeleken;
    }

    /**
     * @return the warnaBolaMata
     */
    public String getWarnaBolaMata() {
        return warnaBolaMata;
    }

    /**
     * @param warnaBolaMata the warnaBolaMata to set
     */
    public void setWarnaBolaMata(String warnaBolaMata) {
        this.warnaBolaMata = warnaBolaMata;
    }

    /**
     * @return the jenisMata
     */
    public String getJenisMata() {
        return jenisMata;
    }

    /**
     * @param jenisMata the jenisMata to set
     */
    public void setJenisMata(String jenisMata) {
        this.jenisMata = jenisMata;
    }

    

}
